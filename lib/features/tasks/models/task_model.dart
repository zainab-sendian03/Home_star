class Task {
  final String name;
  final int points;
  final String time;
  final String date;
  final bool repeat;
  final String childId;
  final String childName;

  Task({
    required this.name,
    required this.points,
    required this.time,
    required this.date,
    required this.repeat,
    required this.childId,
    required this.childName,
  });
}
